const $ = (sel, root = document) => root.querySelector(sel);
const $$ = (sel, root = document) => Array.from(root.querySelectorAll(sel));

function safeSetAttr(el, name, value) {
  if (!el) return;
  el.setAttribute(name, value);
}

function throttle(fn, wait = 100) {
  let last = 0;
  return function (...args) {
    const now = Date.now();
    if (now - last >= wait) {
      last = now;
      fn.apply(this, args);
    }
  };
}

/* -------------------------
    THEME (DARK / LIGHT) LOGIC
    ------------------------- */
function applyTheme(isDark) {
  const body = document.body;
  body.classList.toggle('dark-mode', !!isDark);

  // Toggle icon
  const darkButton = document.getElementById(SELECTORS.darkToggleId);
  if (darkButton) {
    darkButton.textContent = isDark ? ICON.sun : ICON.moon;
    safeSetAttr(darkButton, 'aria-pressed', isDark ? 'true' : 'false');
  }

  // Video swap (if present)
  const videoSource = document.getElementById(SELECTORS.bgVideoSourceId);
  const videoEl = $(SELECTORS.bgVideoSelector);
  if (videoSource && videoEl) {
    const next = isDark ? VIDEO.night : VIDEO.day;
    if (videoSource.getAttribute('src') !== next) {
      videoSource.setAttribute('src', next);
      // reload and attempt to play; guard for promise
      try {
        videoEl.load();
        const p = videoEl.play();
        if (p && p.catch) p.catch(() => { /* autoplay blocked */ });
      } catch (e) { /* ignore */ }
    }
  }

  // store preference
  try {
    localStorage.setItem('theme', isDark ? 'dark' : 'light');
  } catch (e) { /* safari private mode may throw */ }
}

function loadThemeOnStart() {
  let saved = null;
  try { saved = localStorage.getItem('theme'); } catch (e) { /* ignore */ }

  const prefersDark = window.matchMedia && window.matchMedia('(prefers-color-scheme: dark)').matches;
  const isDark = saved === 'dark' || (!saved && prefersDark);
  applyTheme(isDark);
}

/* -------------------------
    MOBILE MENU TOGGLE
    ------------------------- */
function initMenuToggle() {
  const toggle = $(SELECTORS.menuToggle);
  const navPanel = $(SELECTORS.navLinksPanel);

  if (!toggle || !navPanel) return;

  // Ensure ARIA attributes
  safeSetAttr(toggle, 'role', 'button');
  safeSetAttr(toggle, 'aria-label', 'Toggle menu');
  safeSetAttr(toggle, 'aria-expanded', 'false');

  toggle.addEventListener('click', () => {
    const isActive = toggle.classList.toggle('active');
    navPanel.classList.toggle('active', isActive);
    safeSetAttr(toggle, 'aria-expanded', String(isActive));
  });

  // Close menu when a nav link is clicked (delegation)
  navPanel.addEventListener('click', (ev) => {
    const el = ev.target.closest('a');
    if (!el) return;
    // close panel (mobile)
    toggle.classList.remove('active');
    navPanel.classList.remove('active');
    safeSetAttr(toggle, 'aria-expanded', 'false');
  });

  // Accessibility: close menu with Escape
  document.addEventListener('keydown', (e) => {
    if (e.key === 'Escape') {
      // Close hamburger menu
      if (toggle.classList.contains('active')) {
        toggle.classList.remove('active');
        navPanel.classList.remove('active');
        safeSetAttr(toggle, 'aria-expanded', 'false');
      }
      
      // also close language dropdown if open
      const langC = $(SELECTORS.langContainer);
      if (langC && langC.classList.contains('active')) {
        langC.classList.remove('active');
        const langBtn = document.getElementById(SELECTORS.langButtonId);
        safeSetAttr(langBtn, 'aria-expanded', 'false');
      }
    }
  });
}

/* -------------------------
    SMOOTH SCROLL (header offset handling)
    ------------------------- */
function getHeaderOffset() {
  const header = $(SELECTORS.header);
  if (!header) return 0;
  // use header height + small padding
  return Math.ceil(header.getBoundingClientRect().height + 10);
}

function scrollToHash(hash) {
  if (!hash) return;
  const id = hash.replace(/^#/, '');
  const target = document.getElementById(id);
  if (!target) return;
  const yOffset = getHeaderOffset();
  const rect = target.getBoundingClientRect();
  const targetY = window.scrollY + rect.top - yOffset;
  window.scrollTo({ top: targetY, behavior: 'smooth' });
}

/* Intercept anchor clicks for smooth scrolling */
function initAnchorSmoothScroll() {
  document.addEventListener('click', (e) => {
    const a = e.target.closest('a[href^="#"]');
    if (!a) return;
    const href = a.getAttribute('href') || '';
    if (href.length <= 1) return; // '#' or empty, let default
    // If link points to id on this page, handle smoothly
    const samePage = href.startsWith('#') && document.getElementById(href.slice(1));
    if (samePage) {
      e.preventDefault();
      scrollToHash(href);
    }
  });
}

